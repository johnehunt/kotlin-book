package com.jjh.util

/**
 * Object illustrating common object data
 * and behaviour.
 */
object MathUtils {
    // Specifying useful read-only properties
    val ZERO = 0
    val MIN: Int = -100
    val MAX = 100

    // Providing utility member functions
    fun add(x: Int, y: Int) = x + y

    fun sub(x: Int, y: Int) = x - y
}